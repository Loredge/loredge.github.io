function makeLicense(str) {
    var tag = document.createElement('h4');
    tag.className = 'f3';
    tag.innerText = str;
    return tag;
}
function insertLicenses() {
    var content = document.getElementsByClassName('content contact');
    for(var i = 0; i < licenses.length; i++) {
        content[0].append(makeLicense(licenses[i]));
    }
}
